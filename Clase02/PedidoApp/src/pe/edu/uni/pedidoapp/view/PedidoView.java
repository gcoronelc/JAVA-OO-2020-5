  private void btnProcesarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProcesarActionPerformed
    // Validaci√≥n
		if( Validar.isDouble(txtImporte.getText()) == false ){
			txtImporte.setBackground(Color.PINK);
			txtImporte.requestFocus();
			return;
		} else {
			txtImporte.setBackground(Color.WHITE);
		}
		// Previo
		btnProcesar.setEnabled(false);
		btnReset.setEnabled(true);
		txtImporte.setEnabled(false);
		// Variables
		double importe, impuesto, total;
		PedidoService pedidoService;
		// Datos
		importe = Double.parseDouble(txtImporte.getText());
		// Proceso
		pedidoService = new PedidoService();
		impuesto = pedidoService.calcImpuesto(importe);
		total = pedidoService.calcTotal(importe);
		// Reporte
		if (impuesto == -1 || total == -1) {
			txtImpuesto.setText("Error");
			txtTotal.setText("Error");
		} else {
			txtImpuesto.setText("" + impuesto);
			txtTotal.setText("" + total);
		}

  }//GEN-LAST:event_btnProcesarActionPerformed

  private void btnResetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnResetActionPerformed
		btnReset.setEnabled(false);
		btnProcesar.setEnabled(true);
		txtImporte.setEnabled(true);
		txtImporte.setText("");
		txtImpuesto.setText("");
		txtTotal.setText("");
		txtImporte.requestFocus();
  }//GEN-LAST:event_btnResetActionPerformed

