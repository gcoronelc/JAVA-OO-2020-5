    private void btnPromedioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPromedioActionPerformed
        // Validar
        boolean c1 = EduValidar.valIntPositivo(txtNum1);
        boolean c2 = EduValidar.valIntPositivo(txtNum2);
        boolean c3 = EduValidar.valIntPositivo(txtNum3);
        boolean c4 = EduValidar.valIntPositivo(txtNum4);
        boolean c5 = EduValidar.valIntPositivo(txtNum5);
        if (c1 == false || c2 == false || c3 == false || c4 == false || c5 == false) {
            EduValidar.mostrarError(rootPane, "Haaa Pendeivis eres, Ingresar Solo Numeros MÃ­nimo 0");
            return;
        }
        // Variables
        double pro;
        double promedio;
        EduModel model = new EduModel();
        model.setNum1(Integer.parseInt(txtNum1.getText()));
        model.setNum2(Integer.parseInt(txtNum2.getText()));
        model.setNum3(Integer.parseInt(txtNum3.getText()));
        model.setNum4(Integer.parseInt(txtNum4.getText()));
        model.setNum5(Integer.parseInt(txtNum5.getText()));
        // Proceso
        EduController ec = new EduController();
        ec.procesar(model);
        // Promedio
        pro = EduController.promedio(model.getNum1(), model.getNum2(), model.getNum3(), model.getNum4(), model.getNum5());
        if (pro == 0 || pro == 1) {
            EduValidar.mostrarError(rootPane, "No juegues. Imposible sacar promedio ingrese minimo dos numeros mayores a 0");
            return;
        }
        promedio = model.getSuma() / pro;
        model.setPromedio(promedio);
        // Reporte
        txtPromedio.setText("" + model.getPromedio());
    }//GEN-LAST:event_btnPromedioActionPerformed

